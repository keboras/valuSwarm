def get_playbook(*, category: str | None = None, tag: str | None = None) -> list[dict]:
    items = WEALTH_PLAYBOOK
    if category and category in PLAYBOOK_CATEGORIES:
        items = [p for p in items if p["category"] == category]
    if tag:
        items = [p for p in items if tag in (p.get("tags") or [])]
    return items


def get_playbook_entry(entry_id: str) -> dict | None:
    for p in WEALTH_PLAYBOOK:
        if p["id"] == entry_id:
            return p
    return None
